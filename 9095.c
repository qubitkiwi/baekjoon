#include <stdio.h>
#include <stdlib.h>

int main()
{
    int T, n;
    int *x;
    int i, test_case;

    scanf("%d", &T);

    for(test_case=0; test_case<T; test_case++)
    {
        scanf("%d", &n);

        x = calloc(n, sizeof(int));

        x[0] = 1;
        x[1] = 2;
        x[2] = 4;

        for(i=3; i<n; i++)
        {
            x[i] = x[i-1] + x[i-2] + x[i-3];
        }

        printf("%d\n", x[n-1]);
        free(x);
    }


    return 0;
}